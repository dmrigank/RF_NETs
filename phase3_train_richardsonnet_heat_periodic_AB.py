# 3) Models: DeepSets encoders + DeepONet-style dot-product decoder
# ============================================================

class MLP(nn.Module):
    def __init__(self, in_dim: int, out_dim: int, hidden: Tuple[int, ...] = (128, 128), act=nn.Tanh):
        super().__init__()
        layers: List[nn.Module] = []
        dims = (in_dim,) + hidden + (out_dim,)
        for i in range(len(dims) - 2):
            layers.append(nn.Linear(dims[i], dims[i+1]))
            layers.append(act())
        layers.append(nn.Linear(dims[-2], dims[-1]))
        self.net = nn.Sequential(*layers)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


class TwoSetDeepONet(nn.Module):
    """
    Encodes set A and set B separately (DeepSets), fuses latents, then dot-product with trunk features.
    """
    def __init__(
        self,
        latent_dim: int = 128,
        point_hidden: Tuple[int, ...] = (128, 128),
        trunk_hidden: Tuple[int, ...] = (128, 128),
        fuse_hidden: Tuple[int, ...] = (128, 128),
        act=nn.Tanh,
        use_A: bool = True,
        use_B: bool = True,
    ):
        super().__init__()
        self.latent_dim = latent_dim
        self.use_A = use_A
        self.use_B = use_B

        # Point encoder: [x, u, alpha, t] -> latent_dim
        self.point_mlp = MLP(4, latent_dim, hidden=point_hidden, act=act)

        # Fuse: [zA, zB] -> z
        self.fuse_mlp = MLP(2 * latent_dim, latent_dim, hidden=fuse_hidden, act=act)

        # Trunk: [xq, alpha, t] -> latent_dim
        self.trunk_mlp = MLP(3, latent_dim, hidden=trunk_hidden, act=act)

    def encode_set(self, x: torch.Tensor, u: torch.Tensor, alpha: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
        """
        x,u: [B,N], alpha,t: [B,1] -> z: [B,latent_dim]
        """
        B, N = x.shape
        alpha_ = alpha.view(B, 1).expand(B, N)
        t_ = t.view(B, 1).expand(B, N)
        feats = torch.stack([x, u, alpha_, t_], dim=-1)        # [B,N,4]
        e = self.point_mlp(feats.view(B * N, 4)).view(B, N, self.latent_dim)  # [B,N,latent]
        z = e.mean(dim=1)                                      # [B,latent]
        return z

    def forward(
        self,
        xA: torch.Tensor, uA: torch.Tensor,     # [B,N]
        xB: torch.Tensor, uB: torch.Tensor,     # [B,N]
        xq: torch.Tensor,                        # [B,Q]
        alpha: torch.Tensor, t: torch.Tensor,    # [B,1]
    ) -> torch.Tensor:
        B, Q = xq.shape

        if self.use_A:
            zA = self.encode_set(xA, uA, alpha, t)
        else:
            zA = torch.zeros(B, self.latent_dim, device=xq.device, dtype=xq.dtype)

        if self.use_B:
            zB = self.encode_set(xB, uB, alpha, t)
        else:
            zB = torch.zeros(B, self.latent_dim, device=xq.device, dtype=xq.dtype)

        z = self.fuse_mlp(torch.cat([zA, zB], dim=1))  # [B,latent]

        # Trunk
        alpha_q = alpha.view(B, 1).expand(B, Q)
        t_q = t.view(B, 1).expand(B, Q)
        trunk_in = torch.stack([xq, alpha_q, t_q], dim=-1).view(B * Q, 3)  # [B*Q,3]
        tau = self.trunk_mlp(trunk_in).view(B, Q, self.latent_dim)         # [B,Q,latent]

        # Dot product
        u_hat = torch.sum(tau * z.view(B, 1, self.latent_dim), dim=-1)     # [B,Q]
        return u_hat


class OneSetDeepONet(nn.Module):
    """
    Encodes a single union set (DeepSets), then dot-product with trunk features.
    """
    def __init__(
        self,
        latent_dim: int = 128,
        point_hidden: Tuple[int, ...] = (128, 128),
        trunk_hidden: Tuple[int, ...] = (128, 128),
        act=nn.Tanh,
    ):
        super().__init__()
        self.latent_dim = latent_dim
        self.point_mlp = MLP(4, latent_dim, hidden=point_hidden, act=act)
        self.trunk_mlp = MLP(3, latent_dim, hidden=trunk_hidden, act=act)

    def encode_set(self, x: torch.Tensor, u: torch.Tensor, alpha: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
        B, N = x.shape
        alpha_ = alpha.view(B, 1).expand(B, N)
        t_ = t.view(B, 1).expand(B, N)
        feats = torch.stack([x, u, alpha_, t_], dim=-1)                     # [B,N,4]
        e = self.point_mlp(feats.view(B * N, 4)).view(B, N, self.latent_dim)
        return e.mean(dim=1)                                                # [B,latent]

    def forward(self, xU: torch.Tensor, uU: torch.Tensor, xq: torch.Tensor, alpha: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
        B, Q = xq.shape
        z = self.encode_set(xU, uU, alpha, t)                               # [B,latent]

        alpha_q = alpha.view(B, 1).expand(B, Q)
        t_q = t.view(B, 1).expand(B, Q)
        trunk_in = torch.stack([xq, alpha_q, t_q], dim=-1).view(B * Q, 3)
        tau = self.trunk_mlp(trunk_in).view(B, Q, self.latent_dim)

        return torch.sum(tau * z.view(B, 1, self.latent_dim), dim=-1)       # [B,Q]


