@torch.no_grad()
def compute_batch_metrics(u_hat: torch.Tensor, u_true: torch.Tensor) -> Dict[str, float]:
    """
    u_hat,u_true: [B,Q]
    """
    err = u_hat - u_true
    rmse = torch.sqrt(torch.mean(err**2, dim=1))        # [B]
    linf = torch.max(torch.abs(err), dim=1).values      # [B]
    return {
        "rmse_mean": float(rmse.mean().item()),
        "rmse_median": float(rmse.median().item()),
        "linf_mean": float(linf.mean().item()),
        "linf_median": float(linf.median().item()),
    }


def make_xref(Q: int, device: torch.device, dtype: torch.dtype) -> torch.Tensor:
    # uniform [0,1) grid
    x = torch.linspace(0.0, 1.0, Q + 1, device=device, dtype=dtype)[:-1]
    return x


def train_one_method(
    method: str,
    train_loader: DataLoader,
    val_loader: DataLoader,
    xA: torch.Tensor, xB: torch.Tensor,   # [N] constants on CPU
    x_ref: torch.Tensor,                  # [Q] on device
    K: int,
    out_dir: str,
    epochs: int = 2000,
    lr: float = 3e-4,
    weight_decay: float = 1e-4,
    latent_dim: int = 128,
    lambda_rich: float = 1.0,
    p: int = 2,
    num_rich: int = 32,
    rich_mode: str = "supervised",  # supervised | self
    use_amp: bool = True,
    device: str = "cuda",
    seed: int = 0,
) -> Dict[str, Any]:

    torch.manual_seed(seed)
    np.random.seed(seed)

    dev = torch.device(device)
    dtype = x_ref.dtype

    # Build model
    if method == "union":
        model = OneSetDeepONet(latent_dim=latent_dim)
    elif method == "A_only":
        model = TwoSetDeepONet(latent_dim=latent_dim, use_A=True, use_B=False)
    elif method == "B_only":
        model = TwoSetDeepONet(latent_dim=latent_dim, use_A=False, use_B=True)
    elif method in ["rich_sup", "rich_self"]:
        model = TwoSetDeepONet(latent_dim=latent_dim, use_A=True, use_B=True)
    else:
        raise ValueError(f"Unknown method: {method}")

    model.to(dev)

    opt = torch.optim.AdamW(model.parameters(), lr=lr, weight_decay=weight_decay)
    sched = torch.optim.lr_scheduler.ReduceLROnPlateau(opt, mode="min", factor=0.5, patience=50, threshold=1e-5)
    scaler = torch.amp.GradScaler(enabled=(use_amp and dev.type == "cuda"))
    mse = nn.MSELoss()

    # constants to device
    xA_dev = xA.to(dev)
    xB_dev = xB.to(dev)

    history = {"train": [], "val": []}
    best_val = float("inf")
    best_path = os.path.join(out_dir, f"{method}_best.pt")

    # Pre-sample k indices tensor for exact eval in torch (handled inside exact eval)
    # Training loop
    for epoch in range(1, epochs + 1):
        model.train()
        train_loss = 0.0
        n_train = 0

        for batch in train_loader:
            uA = batch["uA"].to(dev)                # [B,N]
            uB = batch["uB"].to(dev)                # [B,N]
            alpha = batch["alpha"].to(dev)          # [B,1]
            t = batch["t"].to(dev).view(-1, 1)      # [B,1]
            c0 = batch["c0"].to(dev)                # [B,1]
            a_sin = batch["a_sin"].to(dev)          # [B,K]
            b_cos = batch["b_cos"].to(dev)          # [B,K]
            Bsz = uA.shape[0]

            # Query grid batch
            xq = x_ref.view(1, -1).expand(Bsz, -1)  # [B,Q]

            with torch.autocast(device_type=dev.type, enabled=(use_amp and dev.type == "cuda")):
                # Predict
                if method == "union":
                    # Union set
                    xU = torch.cat([xA_dev.view(1, -1).expand(Bsz, -1),
                                    xB_dev.view(1, -1).expand(Bsz, -1)], dim=1)  # [B,2N]
                    uU = torch.cat([uA, uB], dim=1)
                    u_hat = model(xU, uU, xq, alpha, t)  # [B,Q]
                else:
                    u_hat = model(xA_dev.view(1, -1).expand(Bsz, -1), uA,
                                  xB_dev.view(1, -1).expand(Bsz, -1), uB,
                                  xq, alpha, t)          # [B,Q]

                # Truth
                u_true = eval_u_exact_periodic_torch(c0, a_sin, b_cos, alpha, xq, t)  # [B,Q]

                loss = mse(u_hat, u_true)

                # Richardson regularizer (only for rich_* methods)
                if method in ["rich_sup", "rich_self"]:
                    # sample midpoint indices on B grid
                    # idx: [B,num_rich]
                    idx = torch.randint(low=0, high=xA_dev.numel(), size=(Bsz, num_rich), device=dev)
                    # gather coordinates
                    x_mid = xB_dev[idx]  # [B,M]
                    x_left = xA_dev[idx] # [B,M]
                    x_right = xA_dev[(idx + 1) % xA_dev.numel()]  # [B,M]

                    # predict at those points
                    x_trip = torch.cat([x_left, x_right, x_mid], dim=1)  # [B,3M]
                    u_trip = model(xA_dev.view(1, -1).expand(Bsz, -1), uA,
                                   xB_dev.view(1, -1).expand(Bsz, -1), uB,
                                   x_trip, alpha, t)  # [B,3M]
                    u_left = u_trip[:, :num_rich]
                    u_right = u_trip[:, num_rich:2*num_rich]
                    u_mid = u_trip[:, 2*num_rich:]

                    u_coarse = 0.5 * (u_left + u_right)
                    u_fine = u_mid
                    r = 2.0
                    u_rich = (r**p * u_fine - u_coarse) / (r**p - 1.0)

                    if method == "rich_self" or rich_mode == "self":
                        loss_rich = mse(u_rich, u_fine)
                    else:
                        u_mid_true = eval_u_exact_periodic_torch(c0, a_sin, b_cos, alpha, x_mid, t)  # [B,M]
                        loss_rich = mse(u_rich, u_mid_true)

                    loss = loss + lambda_rich * loss_rich

            opt.zero_grad(set_to_none=True)
            scaler.scale(loss).backward()
            scaler.step(opt)
            scaler.update()

            train_loss += float(loss.item()) * Bsz
            n_train += Bsz

        train_loss /= max(1, n_train)

        # Validation
        model.eval()
        val_loss = 0.0
        n_val = 0
        with torch.no_grad():
            for batch in val_loader:
                uA = batch["uA"].to(dev)
                uB = batch["uB"].to(dev)
                alpha = batch["alpha"].to(dev)
                t = batch["t"].to(dev).view(-1, 1)
                c0 = batch["c0"].to(dev)
                a_sin = batch["a_sin"].to(dev)
                b_cos = batch["b_cos"].to(dev)
                Bsz = uA.shape[0]
                xq = x_ref.view(1, -1).expand(Bsz, -1)

                with torch.autocast(device_type=dev.type, enabled=(use_amp and dev.type == "cuda")):
                    if method == "union":
                        xU = torch.cat([xA_dev.view(1, -1).expand(Bsz, -1),
                                        xB_dev.view(1, -1).expand(Bsz, -1)], dim=1)
                        uU = torch.cat([uA, uB], dim=1)
                        u_hat = model(xU, uU, xq, alpha, t)
                    else:
                        u_hat = model(xA_dev.view(1, -1).expand(Bsz, -1), uA,
                                      xB_dev.view(1, -1).expand(Bsz, -1), uB,
                                      xq, alpha, t)

                    u_true = eval_u_exact_periodic_torch(c0, a_sin, b_cos, alpha, xq, t)
                    loss = mse(u_hat, u_true)

                val_loss += float(loss.item()) * Bsz
                n_val += Bsz

        val_loss /= max(1, n_val)
        sched.step(val_loss)

        history["train"].append(train_loss)
        history["val"].append(val_loss)

        if (epoch == 1) or (epoch % 50 == 0):
            lr_now = opt.param_groups[0]["lr"]
            print(f"[{method}] epoch {epoch:05d}/{epochs}  train={train_loss:.4e}  val={val_loss:.4e}  lr={lr_now:.2e}")

        if val_loss < best_val:
            best_val = val_loss
            torch.save({
                "method": method,
                "model_state": model.state_dict(),
                "latent_dim": latent_dim,
                "history": history,
                "best_val": best_val,
            }, best_path)

    # Plot training curve
    os.makedirs(out_dir, exist_ok=True)
    plt.figure()
    plt.plot(history["train"], label="train")
    plt.plot(history["val"], label="val")
    plt.yscale("log")
    plt.xlabel("epoch")
    plt.ylabel("MSE (log)")
    plt.title(f"Training curve: {method}")
    plt.legend()
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(os.path.join(out_dir, f"{method}_training_curve.png"), dpi=150)
    plt.close()

    return {"best_path": best_path, "history": history, "best_val": best_val}


@torch.no_grad()
def evaluate_method_on_test(
    method: str,
    ckpt_path: str,
    test_loader: DataLoader,
    xA: torch.Tensor, xB: torch.Tensor,
    x_ref: torch.Tensor,
    device: str = "cuda",
    use_amp: bool = True,
    make_snapshots: bool = True,
    snapshot_dir: Optional[str] = None,
    n_snapshots: int = 3,
) -> Dict[str, Any]:

    dev = torch.device(device)

    ckpt = torch.load(ckpt_path, map_location=dev)
    latent_dim = int(ckpt.get("latent_dim", 128))

    if method == "union":
        model = OneSetDeepONet(latent_dim=latent_dim)
    elif method == "A_only":
        model = TwoSetDeepONet(latent_dim=latent_dim, use_A=True, use_B=False)
    elif method == "B_only":
        model = TwoSetDeepONet(latent_dim=latent_dim, use_A=False, use_B=True)
    else:
        # rich_sup or rich_self use same architecture at inference
        model = TwoSetDeepONet(latent_dim=latent_dim, use_A=True, use_B=True)

    model.load_state_dict(ckpt["model_state"])
    model.to(dev).eval()

    xA_dev = xA.to(dev)
    xB_dev = xB.to(dev)
    x_ref = x_ref.to(dev)

    all_rmse = []
    all_linf = []

    for batch in test_loader:
        uA = batch["uA"].to(dev)
        uB = batch["uB"].to(dev)
        alpha = batch["alpha"].to(dev)
        t = batch["t"].to(dev).view(-1, 1)
        c0 = batch["c0"].to(dev)
        a_sin = batch["a_sin"].to(dev)
        b_cos = batch["b_cos"].to(dev)
        Bsz = uA.shape[0]

        xq = x_ref.view(1, -1).expand(Bsz, -1)

        with torch.autocast(device_type=dev.type, enabled=(use_amp and dev.type == "cuda")):
            if method == "union":
                xU = torch.cat([xA_dev.view(1, -1).expand(Bsz, -1),
                                xB_dev.view(1, -1).expand(Bsz, -1)], dim=1)
                uU = torch.cat([uA, uB], dim=1)
                u_hat = model(xU, uU, xq, alpha, t)
            else:
                u_hat = model(xA_dev.view(1, -1).expand(Bsz, -1), uA,
                              xB_dev.view(1, -1).expand(Bsz, -1), uB,
                              xq, alpha, t)

            u_true = eval_u_exact_periodic_torch(c0, a_sin, b_cos, alpha, xq, t)

        err = u_hat - u_true
        rmse = torch.sqrt(torch.mean(err**2, dim=1))  # [B]
        linf = torch.max(torch.abs(err), dim=1).values
        all_rmse.append(rmse.detach().cpu())
        all_linf.append(linf.detach().cpu())

    all_rmse = torch.cat(all_rmse, dim=0).numpy()
    all_linf = torch.cat(all_linf, dim=0).numpy()

    metrics = {
        "rmse_mean": float(all_rmse.mean()),
        "rmse_median": float(np.median(all_rmse)),
        "rmse_std": float(all_rmse.std()),
        "linf_mean": float(all_linf.mean()),
        "linf_median": float(np.median(all_linf)),
        "linf_std": float(all_linf.std()),
        "n_test": int(all_rmse.shape[0]),
    }

    # Snapshot plots (first few batches)
    if make_snapshots:
        if snapshot_dir is None:
            snapshot_dir = os.path.join(os.path.dirname(ckpt_path), "snapshots")
        os.makedirs(snapshot_dir, exist_ok=True)

        snap_done = 0
        for batch in test_loader:
            uA = batch["uA"].to(dev)
            uB = batch["uB"].to(dev)
            alpha = batch["alpha"].to(dev)
            t = batch["t"].to(dev).view(-1, 1)
            c0 = batch["c0"].to(dev)
            a_sin = batch["a_sin"].to(dev)
            b_cos = batch["b_cos"].to(dev)
            Bsz = uA.shape[0]
            xq = x_ref.view(1, -1).expand(Bsz, -1)

            with torch.autocast(device_type=dev.type, enabled=(use_amp and dev.type == "cuda")):
                if method == "union":
                    xU = torch.cat([xA_dev.view(1, -1).expand(Bsz, -1),
                                    xB_dev.view(1, -1).expand(Bsz, -1)], dim=1)
                    uU = torch.cat([uA, uB], dim=1)
                    u_hat = model(xU, uU, xq, alpha, t)
                else:
                    u_hat = model(xA_dev.view(1, -1).expand(Bsz, -1), uA,
                                  xB_dev.view(1, -1).expand(Bsz, -1), uB,
                                  xq, alpha, t)

                u_true = eval_u_exact_periodic_torch(c0, a_sin, b_cos, alpha, xq, t)

            for b in range(Bsz):
                if snap_done >= n_snapshots:
                    break
                x_np = x_ref.detach().cpu().numpy()
                uhat_np = u_hat[b].detach().cpu().numpy()
                utru_np = u_true[b].detach().cpu().numpy()

                plt.figure()
                plt.plot(x_np, utru_np, label="Exact", linewidth=2)
                plt.plot(x_np, uhat_np, "--", label="Model", linewidth=2)
                plt.xlabel("x")
                plt.ylabel("u(x,t)")
                plt.title(f"{method}  sample={snap_done}  t={float(t[b].item()):.3f}  alpha={float(alpha[b].item()):.3f}")
                plt.legend()
                plt.grid(True)
                plt.tight_layout()
                plt.savefig(os.path.join(snapshot_dir, f"{method}_snap_{snap_done:02d}.png"), dpi=150)
                plt.close()
                snap_done += 1

            if snap_done >= n_snapshots:
                break

    return metrics


