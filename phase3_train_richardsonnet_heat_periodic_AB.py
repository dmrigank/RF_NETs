#!/usr/bin/env python3
"""
Phase 3 — Learnable fusion (RichardsonNet-style) on 1D periodic heat equation.

This script trains set-based neural operators to fuse TWO coarse solutions produced by the same
nominal 2nd-order solver (CN + 2nd-order FD), sampled on two different uniform grids:

  Grid A: x_i = i/N
  Grid B: x_i = (i+0.5)/N   (staggered)

It loads Phase-1 .npz files (generated by phase1_generate_heat_periodic_AB.py) containing:
  xA [N], xB [N], t_snap [nt]
  alpha [S], c0 [S], a_sin [S,K], b_cos [S,K]
  uA [S,nt,N], uB [S,nt,N]

Then trains one of the following methods:

  - A_only    : predict truth using only set A = {(xA,uA)}
  - B_only    : predict truth using only set B = {(xB,uB)}
  - union     : predict truth using union set {(xA,uA)} ∪ {(xB,uB)} (no Richardson loss)
  - rich_sup  : two-set fusion + supervised Richardson loss (p=2 by default)
  - rich_self : two-set fusion + self-consistency Richardson loss (regularizer)

All models are permutation-invariant set encoders (DeepSets-style):
  zA = mean_i phi([xA_i, uA_i, alpha, t])
  zB = mean_i phi([xB_i, uB_i, alpha, t])
  z  = psi([zA,zB])
  tau(xq) = trunk([xq, alpha, t])
  u_hat(xq) = <z, tau(xq)>

Supervision uses the exact periodic heat solution computed from stored Fourier coefficients.

Outputs:
  - best.pt checkpoint
  - training_curve.png
  - metrics.json
  - snapshots/*.png (optional)

Example:
  python phase3_train_richardsonnet_heat_periodic_AB.py \
    --npz heat_phase1_data/heat_periodic_AB_CN2_FD2_N100_K16_S2000_T0.2.npz \
    --out_dir heat_phase3_runs \
    --method rich_sup \
    --epochs 2000 --batch_size 64 --q_factor 4 --lambda_rich 1.0 --num_rich 32
"""

