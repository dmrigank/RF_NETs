# ============================================================
# 1) Exact periodic heat solution in torch (vectorized)
# ============================================================

def eval_u_exact_periodic_torch(
    c0: torch.Tensor,          # [B,1]
    a_sin: torch.Tensor,       # [B,K]
    b_cos: torch.Tensor,       # [B,K]
    alpha: torch.Tensor,       # [B,1]
    x: torch.Tensor,           # [B,Q] or [B,M]
    t: torch.Tensor,           # [B,1]
) -> torch.Tensor:
    """
    u(x,t) = c0 + sum_{k=1..K} exp(-alpha*(2πk)^2 t) * (a_k sin(2πk x) + b_k cos(2πk x))
    Returns: [B,Q]
    """
    # Ensure shapes
    if c0.ndim == 1:
        c0 = c0.view(-1, 1)
    if alpha.ndim == 1:
        alpha = alpha.view(-1, 1)
    if t.ndim == 1:
        t = t.view(-1, 1)
    if x.ndim == 1:
        x = x.view(1, -1).expand(c0.shape[0], -1)

    B = c0.shape[0]
    K = a_sin.shape[1]
    device = c0.device
    dtype = c0.dtype

    # k: [K]
    k = torch.arange(1, K + 1, device=device, dtype=dtype).view(1, K, 1)  # [1,K,1]
    two_pi = torch.tensor(2.0 * np.pi, device=device, dtype=dtype)

    # x: [B,Q] -> [B,1,Q]
    x_ = x.view(B, 1, -1)

    # exp factor: [B,K,1]
    lam = (two_pi * k) ** 2                         # [1,K,1]
    fac = torch.exp(-alpha.view(B, 1, 1) * lam * t.view(B, 1, 1))  # [B,K,1]

    # trig: [B,K,Q]
    arg = two_pi * k * x_
    s = torch.sin(arg)
    c = torch.cos(arg)

    # coeffs: [B,K,1]
    a = a_sin.view(B, K, 1)
    b = b_cos.view(B, K, 1)

    u = c0.view(B, 1) + torch.sum(fac * (a * s + b * c), dim=1)  # [B,Q]
    return u


