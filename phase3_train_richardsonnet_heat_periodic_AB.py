def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--npz", type=str, required=True, help="Path to Phase-1 .npz")
    ap.add_argument("--out_dir", type=str, required=True, help="Output directory for this run")
    ap.add_argument("--method", type=str, default="rich_sup",
                    choices=["A_only", "B_only", "union", "rich_sup", "rich_self"],
                    help="Training method")

    ap.add_argument("--epochs", type=int, default=2000)
    ap.add_argument("--batch_size", type=int, default=64)
    ap.add_argument("--lr", type=float, default=3e-4)
    ap.add_argument("--weight_decay", type=float, default=1e-4)
    ap.add_argument("--latent_dim", type=int, default=128)

    ap.add_argument("--q_factor", type=int, default=4, help="Query grid size Q = q_factor * N")
    ap.add_argument("--lambda_rich", type=float, default=1.0)
    ap.add_argument("--p", type=int, default=2)
    ap.add_argument("--num_rich", type=int, default=32, help="Rich triplets per sample per batch")
    ap.add_argument("--rich_mode", type=str, default="supervised", choices=["supervised", "self"])

    ap.add_argument("--device", type=str, default=("cuda" if torch.cuda.is_available() else "cpu"))
    ap.add_argument("--no_amp", action="store_true")
    ap.add_argument("--seed", type=int, default=0)

    ap.add_argument("--make_snapshots", action="store_true")
    ap.add_argument("--n_snapshots", type=int, default=3)

    args = ap.parse_args()

    os.makedirs(args.out_dir, exist_ok=True)
    with open(os.path.join(args.out_dir, "config.json"), "w") as f:
        json.dump(vars(args), f, indent=2)

    # Load datasets
    train_ds = HeatABDataset(args.npz, split="train", seed=args.seed)
    val_ds = HeatABDataset(args.npz, split="val", seed=args.seed)
    test_ds = HeatABDataset(args.npz, split="test", seed=args.seed)

    # DataLoaders
    train_loader = DataLoader(train_ds, batch_size=args.batch_size, shuffle=True, num_workers=0, collate_fn=collate_batch)
    val_loader = DataLoader(val_ds, batch_size=args.batch_size, shuffle=False, num_workers=0, collate_fn=collate_batch)
    test_loader = DataLoader(test_ds, batch_size=args.batch_size, shuffle=False, num_workers=0, collate_fn=collate_batch)

    # Constants
    xA = train_ds.xA
    xB = train_ds.xB
    N = int(xA.numel())
    K = int(train_ds.a_sin.shape[1])

    dev = torch.device(args.device)
    x_ref = make_xref(args.q_factor * N, dev, dtype=torch.float32)

    # Train
    result = train_one_method(
        method=args.method,
        train_loader=train_loader,
        val_loader=val_loader,
        xA=xA, xB=xB,
        x_ref=x_ref,
        K=K,
        out_dir=args.out_dir,
        epochs=args.epochs,
        lr=args.lr,
        weight_decay=args.weight_decay,
        latent_dim=args.latent_dim,
        lambda_rich=args.lambda_rich,
        p=args.p,
        num_rich=args.num_rich,
        rich_mode=args.rich_mode,
        use_amp=(not args.no_amp),
        device=args.device,
        seed=args.seed,
    )

    # Evaluate
    metrics = evaluate_method_on_test(
        method=args.method,
        ckpt_path=result["best_path"],
        test_loader=test_loader,
        xA=xA, xB=xB,
        x_ref=x_ref,
        device=args.device,
        use_amp=(not args.no_amp),
        make_snapshots=args.make_snapshots,
        snapshot_dir=os.path.join(args.out_dir, "snapshots"),
        n_snapshots=args.n_snapshots,
    )

    out = {
        "npz": args.npz,
        "method": args.method,
        "N": N,
        "K": K,
        "Q": int(x_ref.numel()),
        "best_val": float(result["best_val"]),
        "test_metrics": metrics,
        "best_ckpt": result["best_path"],
    }
    with open(os.path.join(args.out_dir, "metrics.json"), "w") as f:
        json.dump(out, f, indent=2)

    print("\n=== DONE ===")
    print(json.dumps(out, indent=2))


if __name__ == "__main__":
    main()
