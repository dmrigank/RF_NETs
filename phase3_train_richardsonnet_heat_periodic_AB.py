# 2) Dataset: loads Phase-1 .npz and creates (sample,time) pairs
# ============================================================

class HeatABDataset(Dataset):
    def __init__(
        self,
        npz_path: str,
        split: str,
        seed: int = 0,
        train_frac: float = 0.8,
        val_frac: float = 0.1,
        dtype: torch.dtype = torch.float32,
    ):
        super().__init__()
        self.npz_path = npz_path
        self.data = np.load(npz_path)

        # Required keys
        for k in ["xA", "xB", "t_snap", "alpha", "c0", "a_sin", "b_cos", "uA", "uB"]:
            if k not in self.data:
                raise KeyError(f"Missing key '{k}' in {npz_path}")

        # Constants / arrays
        self.xA = torch.tensor(self.data["xA"], dtype=dtype)       # [N]
        self.xB = torch.tensor(self.data["xB"], dtype=dtype)       # [N]
        self.t_snap = torch.tensor(self.data["t_snap"], dtype=dtype)  # [nt]

        # Per-sample params + coefficients
        self.alpha = torch.tensor(self.data["alpha"], dtype=dtype).view(-1, 1)  # [S,1]
        self.c0 = torch.tensor(self.data["c0"], dtype=dtype).view(-1, 1)        # [S,1]
        self.a_sin = torch.tensor(self.data["a_sin"], dtype=dtype)              # [S,K]
        self.b_cos = torch.tensor(self.data["b_cos"], dtype=dtype)              # [S,K]

        # Solver outputs (store on CPU)
        self.uA = torch.tensor(self.data["uA"], dtype=dtype)  # [S,nt,N]
        self.uB = torch.tensor(self.data["uB"], dtype=dtype)  # [S,nt,N]

        self.S, self.nt, self.N = self.uA.shape
        assert self.uB.shape == (self.S, self.nt, self.N)

        # Split by sample id (avoid leakage across times)
        rng = np.random.default_rng(seed)
        perm = rng.permutation(self.S)

        n_train = int(train_frac * self.S)
        n_val = int(val_frac * self.S)
        n_test = self.S - n_train - n_val

        if split == "train":
            self.sample_ids = perm[:n_train]
        elif split == "val":
            self.sample_ids = perm[n_train:n_train + n_val]
        elif split == "test":
            self.sample_ids = perm[n_train + n_val:]
        else:
            raise ValueError("split must be one of: train, val, test")

        self.sample_ids = torch.tensor(self.sample_ids, dtype=torch.long)
        # dataset length: (#sample_ids) * nt
        self.length = int(self.sample_ids.numel() * self.nt)

    def __len__(self) -> int:
        return self.length

    def __getitem__(self, idx: int):
        # map idx -> (local_sample_index, time_index)
        local_s = idx // self.nt
        j = idx % self.nt
        sid = int(self.sample_ids[local_s].item())

        uA_j = self.uA[sid, j, :]  # [N]
        uB_j = self.uB[sid, j, :]  # [N]
        alpha = self.alpha[sid, :] # [1]
        t = self.t_snap[j].view(1) # [1]

        c0 = self.c0[sid, :]       # [1]
        a = self.a_sin[sid, :]     # [K]
        b = self.b_cos[sid, :]     # [K]

        return {
            "uA": uA_j,
            "uB": uB_j,
            "alpha": alpha,
            "t": t,
            "c0": c0,
            "a_sin": a,
            "b_cos": b,
        }


def collate_batch(batch: List[Dict[str, torch.Tensor]]) -> Dict[str, torch.Tensor]:
    out: Dict[str, torch.Tensor] = {}
    for k in batch[0].keys():
        out[k] = torch.stack([b[k] for b in batch], dim=0)
    return out


